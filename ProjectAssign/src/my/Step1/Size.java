    private void jButtonBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBackActionPerformed
        //brings you back to which ever frame you where in\\
        if (my.Step1.Speciality.specialFlag==true){
            Speciality Spec=new Speciality  ();
            Spec.setVisible (true) ;
            setVisible (false) ;
            dispose ();

        }
        else if(my.Step1.Brewed.brewedFlag==true){
            Brewed Bre=new Brewed ();
            Bre.setVisible (true) ;
            setVisible (false) ;
            dispose ();
        }
        else if(my.Step1.Teas.teaFlag==true){
            Teas tea=new Teas ();
            tea.setVisible (true) ;
            setVisible (false) ;
            dispose ();

        }
       
    }//GEN-LAST:event_jButtonBackActionPerformed

